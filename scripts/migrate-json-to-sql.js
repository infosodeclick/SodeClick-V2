#!/usr/bin/env node
/*
  JSON -> SQL seed exporter (safe, no direct DB write)
  Output: db/seed_from_json.sql
*/
const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const dataDir = path.join(root, 'data');
const outFile = path.join(root, 'db', 'seed_from_json.sql');

function readJson(name, fallback = []) {
  const p = path.join(dataDir, name);
  if (!fs.existsSync(p)) return fallback;
  try {
    return JSON.parse(fs.readFileSync(p, 'utf8'));
  } catch {
    return fallback;
  }
}

function esc(v) {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return Number.isFinite(v) ? String(v) : 'NULL';
  if (typeof v === 'boolean') return v ? 'TRUE' : 'FALSE';
  const s = String(v).replace(/'/g, "''");
  return `'${s}'`;
}

function row(cols) {
  return `(${cols.map(esc).join(', ')})`;
}

function chunkInsert(table, columns, values, chunks = 500) {
  if (!values.length) return '';
  const out = [];
  for (let i = 0; i < values.length; i += chunks) {
    const part = values.slice(i, i + chunks).map((r) => row(r)).join(',\n  ');
    out.push(`INSERT INTO ${table} (${columns.join(', ')}) VALUES\n  ${part};`);
  }
  return out.join('\n\n');
}

const users = readJson('users.json');
const likes = readJson('likes.json');
const matches = readJson('matches.json');
const messages = readJson('messages.json');
const reports = readJson('reports.json');
const blocks = readJson('blocks.json');
const coinTx = readJson('coin-transactions.json');
const boardPosts = readJson('board-posts.json');

const sql = [];
sql.push('-- Auto-generated by scripts/migrate-json-to-sql.js');
sql.push(`-- Generated at: ${new Date().toISOString()}`);
sql.push('BEGIN;\n');

// roles baseline
sql.push(`INSERT INTO roles (name, description) VALUES
  ('user','default user'),
  ('vip','vip user'),
  ('admin','admin user'),
  ('moderator','moderator user')
ON CONFLICT (name) DO NOTHING;\n`);

// users + profiles + wallets
const userRows = users.map((u) => [
  u.username || `user_${Math.random().toString(36).slice(2, 7)}`,
  u.email || `${u.username || 'user'}@example.local`,
  u.phone || null,
  u.password || u.password_hash || 'CHANGE_ME',
  'active',
  null,
  !!u.emailVerified,
  !!u.phoneVerified,
  !!u.verifiedBadge,
  new Date(u.createdAt || Date.now()).toISOString(),
  new Date(u.updatedAt || Date.now()).toISOString(),
]);

sql.push(chunkInsert(
  'users',
  ['username','email','phone','password_hash','status','role_id','is_email_verified','is_phone_verified','is_selfie_verified','created_at','updated_at'],
  userRows,
));

// profiles from users
const profileRows = users.map((u) => [
  u.username || '',
  u.displayName || u.username || '',
  u.bio || '',
  u.gender || null,
  u.age || null,
  u.location || null,
  u.occupation || null,
  u.relationshipGoal || null,
  u.status || 'offline',
]);

sql.push(`
-- user_profiles via username join
INSERT INTO user_profiles (user_id, display_name, bio, gender, age, city, occupation, relationship_goal_id, online_status)
SELECT u.id, v.display_name, v.bio, v.gender, v.age, v.city, v.occupation, NULL, v.online_status
FROM (
  VALUES
  ${profileRows.map((r) => row(r)).join(',\n  ')}
) AS v(username, display_name, bio, gender, age, city, occupation, relationship_goal, online_status)
JOIN users u ON u.username = v.username;
`);

const walletRows = users.map((u) => [u.username || '', u.coins || 0]);
sql.push(`
INSERT INTO user_wallets (user_id, balance)
SELECT u.id, v.balance
FROM (
  VALUES
  ${walletRows.map((r) => row(r)).join(',\n  ')}
) AS v(username, balance)
JOIN users u ON u.username = v.username
ON CONFLICT (user_id) DO UPDATE SET balance = EXCLUDED.balance;
`);

// likes
if (likes.length) {
  const likeRows = likes.map((x) => [x.from, x.to, x.type || 'like', new Date(x.at || Date.now()).toISOString()]);
  sql.push(`
INSERT INTO user_likes (from_user_id, to_user_id, like_type, created_at)
SELECT uf.id, ut.id, v.like_type, v.created_at::timestamptz
FROM (
  VALUES
  ${likeRows.map((r) => row(r)).join(',\n  ')}
) AS v(from_username, to_username, like_type, created_at)
JOIN users uf ON uf.username = v.from_username
JOIN users ut ON ut.username = v.to_username;
`);
}

// matches
if (matches.length) {
  const matchRows = matches.map((m) => [m.userA, m.userB, new Date(m.at || Date.now()).toISOString()]);
  sql.push(`
INSERT INTO matches (user1_id, user2_id, matched_at, status)
SELECT u1.id, u2.id, v.matched_at::timestamptz, 'active'
FROM (
  VALUES
  ${matchRows.map((r) => row(r)).join(',\n  ')}
) AS v(user_a, user_b, matched_at)
JOIN users u1 ON u1.username = v.user_a
JOIN users u2 ON u2.username = v.user_b
ON CONFLICT DO NOTHING;
`);
}

// simple board topics/comments
if (boardPosts.length) {
  const topicRows = boardPosts.map((p) => [p.author || 'user', p.title || 'Untitled', p.content || '', p.category || 'general', p.likes || 0, p.reports || 0, !!p.pinned, new Date(p.createdAt || Date.now()).toISOString()]);
  sql.push(`
INSERT INTO board_topics (category_id, user_id, title, content, like_count, view_count, comment_count, is_pinned, status, created_at)
SELECT NULL, u.id, v.title, v.content, v.like_count, 0, 0, v.is_pinned, 'active', v.created_at::timestamptz
FROM (
  VALUES
  ${topicRows.map((r) => row(r)).join(',\n  ')}
) AS v(author, title, content, category, like_count, reports, is_pinned, created_at)
JOIN users u ON u.username = v.author;
`);
}

// coin tx
if (coinTx.length) {
  const txRows = coinTx.map((t) => [t.username || '', t.type || 'unknown', t.amount || 0, t.note || '', new Date(t.at || Date.now()).toISOString()]);
  sql.push(`
INSERT INTO wallet_transactions (wallet_id, user_id, transaction_type, direction, amount, balance_before, balance_after, note, created_at)
SELECT w.id, u.id, v.transaction_type,
       CASE WHEN v.amount >= 0 THEN 'in' ELSE 'out' END,
       abs(v.amount),
       w.balance, w.balance,
       v.note,
       v.created_at::timestamptz
FROM (
  VALUES
  ${txRows.map((r) => row(r)).join(',\n  ')}
) AS v(username, transaction_type, amount, note, created_at)
JOIN users u ON u.username = v.username
JOIN user_wallets w ON w.user_id = u.id;
`);
}

// reports
if (reports.length) {
  const repRows = reports.map((r) => [r.reporter || '', r.target || '', r.reason || 'reported', new Date(r.at || Date.now()).toISOString()]);
  sql.push(`
INSERT INTO user_reports (reporter_user_id, reported_user_id, detail, status, created_at)
SELECT ur.id, ut.id, v.reason, 'open', v.created_at::timestamptz
FROM (
  VALUES
  ${repRows.map((r) => row(r)).join(',\n  ')}
) AS v(reporter, target, reason, created_at)
JOIN users ur ON ur.username = v.reporter
JOIN users ut ON ut.username = v.target;
`);
}

// blocks
if (blocks.length) {
  const blkRows = blocks.map((b) => [b.owner || '', b.target || '', new Date(b.at || Date.now()).toISOString()]);
  sql.push(`
INSERT INTO user_blocks (user_id, blocked_user_id, created_at)
SELECT uo.id, ut.id, v.created_at::timestamptz
FROM (
  VALUES
  ${blkRows.map((r) => row(r)).join(',\n  ')}
) AS v(owner, target, created_at)
JOIN users uo ON uo.username = v.owner
JOIN users ut ON ut.username = v.target
ON CONFLICT DO NOTHING;
`);
}

sql.push('COMMIT;\n');

if (!fs.existsSync(path.join(root, 'db'))) fs.mkdirSync(path.join(root, 'db'), { recursive: true });
fs.writeFileSync(outFile, sql.join('\n\n'), 'utf8');

console.log(`[ok] exported SQL seed to ${outFile}`);
